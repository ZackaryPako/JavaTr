package day5;

public class Car {

	private String model;
	private String color;
	private int year;
	
	public void setModel(String setmodel) {
		model = setmodel;
	}
	public void setColor(String setcolor) {
		color = setcolor;
	}
	public void setYear(int setyear) {
		year = setyear;
	}
	
	public String getModel() {
		return model;
	}
	public String getColor() {
		return color;
	}
	public int getYearl() {
		return year;
	}
	
}
